class OrderItem
{
    public $item;
    public $size;

    /**
     * OrderItem constructor.
     * @param $item
     * @param $size
     */
    public function __construct($item, $size)
    {
        $this->item = $item;
        $this->size = $size;
    }

    /**
     * @return mixed
     */
    public function getItem()
    {
        return $this->item;
    }

    /**
     * @param mixed $item
     */
    public function setItem($item)
    {
        $this->item = $item;
    }

    /**
     * @return mixed
     */
    public function getSize()
    {
        return $this->size;
    }

    /**
     * @param mixed $size
     */
    public function setSize($size)
    {
        $this->size = $size;
    }
}